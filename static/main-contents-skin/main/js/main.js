class MainContentManager {
    constructor() {
        this.content = null;
        this.currentPage = 1;
        this.isLoading = false;
        this.intersectionObserver = null;

        this.init();
    }

    init() {
        this.content = document.querySelector('.content-main');
        if (!this.content) return;

        this.setupInfiniteScroll();
        this.setupInteractions();
        this.setupAnimations();
        this.setupSearch();
        this.setupFilters();
        this.setupModals();
    }

    setupInfiniteScroll() {
        const loadMoreTrigger = document.querySelector('.load-more-trigger');
        if (!loadMoreTrigger) return;

        this.intersectionObserver = new IntersectionObserver(
            (entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting && !this.isLoading) {
                        this.loadMoreContent();
                    }
                });
            },
            { threshold: 0.1 }
        );

        this.intersectionObserver.observe(loadMoreTrigger);
    }

    async loadMoreContent() {
        if (this.isLoading) return;

        this.isLoading = true;
        this.showLoadingIndicator();

        try {
            const response = await fetch(`/api/content?page=${this.currentPage + 1}`);
            const data = await response.json();

            if (data.items && data.items.length > 0) {
                this.appendContent(data.items);
                this.currentPage++;
            } else {
                this.hideLoadMoreTrigger();
            }
        } catch (error) {
            console.error('Failed to load more content:', error);
            App.notifications.error('ÏΩòÌÖêÏ∏† Î°úÎìú Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.');
        } finally {
            this.isLoading = false;
            this.hideLoadingIndicator();
        }
    }

    appendContent(items) {
        const container = document.querySelector('.content-container');
        if (!container) return;

        items.forEach(item => {
            const element = this.createContentElement(item);
            container.appendChild(element);

            // Ïï†ÎãàÎ©îÏù¥ÏÖò Ìä∏Î¶¨Í±∞
            requestAnimationFrame(() => {
                element.classList.add('fade-in');
            });
        });
    }

    createContentElement(item) {
        const element = document.createElement('div');
        element.className = 'content-item';
        element.innerHTML = `
            <div class="content-card">
                <div class="card-header">
                    <h3 class="card-title">${item.title}</h3>
                    <span class="card-date">${Utils.formatDate(item.date)}</span>
                </div>
                <div class="card-content">
                    <p>${item.excerpt}</p>
                </div>
                <div class="card-footer">
                    <div class="card-meta">
                        <span class="card-author">${item.author}</span>
                        <span class="card-views">${item.views} Ï°∞Ìöå</span>
                    </div>
                    <div class="card-actions">
                        <button class="card-btn primary" onclick="MainContent.viewItem('${item.id}')">
                            ÏûêÏÑ∏Ìûà Î≥¥Í∏∞
                        </button>
                    </div>
                </div>
            </div>
        `;

        return element;
    }

    setupInteractions() {
        // Ï¢ãÏïÑÏöî Î≤ÑÌäº
        this.content.addEventListener('click', (e) => {
            if (e.target.matches('.like-btn')) {
                this.toggleLike(e.target);
            }
        });

        // Í≥µÏú† Î≤ÑÌäº
        this.content.addEventListener('click', (e) => {
            if (e.target.matches('.share-btn')) {
                this.shareContent(e.target);
            }
        });

        // ÎåìÍ∏Ä ÌÜ†Í∏Ä
        this.content.addEventListener('click', (e) => {
            if (e.target.matches('.comment-toggle')) {
                this.toggleComments(e.target);
            }
        });

        // ÌÉ≠ Ï†ÑÌôò
        this.content.addEventListener('click', (e) => {
            if (e.target.matches('.tab-item')) {
                this.switchTab(e.target);
            }
        });
    }

    toggleLike(button) {
        const isLiked = button.classList.contains('liked');
        const countElement = button.querySelector('.like-count');
        let count = parseInt(countElement.textContent) || 0;

        if (isLiked) {
            button.classList.remove('liked');
            count--;
            button.innerHTML = `üëç ${count}`;
        } else {
            button.classList.add('liked');
            count++;
            button.innerHTML = `‚ù§Ô∏è ${count}`;
        }

        // Ïï†ÎãàÎ©îÏù¥ÏÖò Ìö®Í≥º
        button.style.transform = 'scale(1.2)';
        setTimeout(() => {
            button.style.transform = '';
        }, 150);
    }

    shareContent(button) {
        const contentId = button.dataset.contentId;
        const title = button.dataset.title;
        const url = window.location.href;

        if (navigator.share) {
            navigator.share({
                title: title,
                url: url
            });
        } else {
            // Ìè¥Î∞±: ÌÅ¥Î¶ΩÎ≥¥ÎìúÏóê Î≥µÏÇ¨
            navigator.clipboard.writeText(url).then(() => {
                App.notifications.success('ÎßÅÌÅ¨Í∞Ä ÌÅ¥Î¶ΩÎ≥¥ÎìúÏóê Î≥µÏÇ¨ÎêòÏóàÏäµÎãàÎã§.');
            });
        }
    }

    toggleComments(button) {
        const contentItem = button.closest('.content-item');
        const commentsSection = contentItem.querySelector('.comments-section');

        if (!commentsSection) {
            this.loadComments(contentItem, button.dataset.contentId);
        } else {
            commentsSection.style.display =
                commentsSection.style.display === 'none' ? 'block' : 'none';
        }
    }

    async loadComments(contentItem, contentId) {
        try {
            const response = await fetch(`/api/comments/${contentId}`);
            const comments = await response.json();

            const commentsSection = document.createElement('div');
            commentsSection.className = 'comments-section';
            commentsSection.innerHTML = this.renderComments(comments);

            contentItem.appendChild(commentsSection);
        } catch (error) {
            console.error('Failed to load comments:', error);
            App.notifications.error('ÎåìÍ∏Ä Î°úÎìú Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.');
        }
    }

    renderComments(comments) {
        const commentsHtml = comments.map(comment => `
            <div class="comment-item">
                <div class="comment-avatar">${comment.author.charAt(0)}</div>
                <div class="comment-content">
                    <div class="comment-header">
                        <span class="comment-author">${comment.author}</span>
                        <span class="comment-time">${Utils.timeAgo(comment.createdAt)}</span>
                    </div>
                    <div class="comment-text">${comment.text}</div>
                </div>
            </div>
        `).join('');

        return `
            <div class="comments-list">
                ${commentsHtml}
            </div>
            <div class="comment-form">
                <textarea class="comment-input" placeholder="ÎåìÍ∏ÄÏùÑ ÏûÖÎ†•ÌïòÏÑ∏Ïöî..."></textarea>
                <button class="comment-submit">ÎåìÍ∏Ä ÏûëÏÑ±</button>
            </div>
        `;
    }

    switchTab(tabButton) {
        const tabContainer = tabButton.closest('.tab-navigation');
        const tabContent = tabContainer.nextElementSibling;

        // Î™®Îì† ÌÉ≠ ÎπÑÌôúÏÑ±Ìôî
        tabContainer.querySelectorAll('.tab-item').forEach(tab => {
            tab.classList.remove('active');
        });

        // ÏÑ†ÌÉùÎêú ÌÉ≠ ÌôúÏÑ±Ìôî
        tabButton.classList.add('active');

        // ÌÉ≠ ÏΩòÌÖêÏ∏† Ï†ÑÌôò
        const targetTab = tabButton.dataset.tab;
        const contentPanes = tabContent.querySelectorAll('.tab-pane');

        contentPanes.forEach(pane => {
            pane.style.display = pane.id === targetTab ? 'block' : 'none';
        });
    }

    setupAnimations() {
        // ÏΩòÌÖêÏ∏† ÏòÅÏó≠Ïù¥ ÏóÜÏúºÎ©¥ Ïï†ÎãàÎ©îÏù¥ÏÖò ÏÑ§Ï†ï Í±¥ÎÑàÎõ∞Í∏∞
        if (!this.content) return;

        // ÏòàÏãú: Ïä§ÌÅ¨Î°§Ïóê Îî∞Îùº ÏöîÏÜå ÎÇòÌÉÄÎÇòÎäî Ìö®Í≥º
        const animatedElements = this.content.querySelectorAll('.fade-in-on-scroll');
        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    entry.target.classList.add('visible');
                    observer.unobserve(entry.target);
                }
            });
        }, { threshold: 0.1 });

        animatedElements.forEach(el => observer.observe(el));

        // Ìå®Îü¥ÎûôÏä§ Ìö®Í≥º
        this.setupParallax();
    }

    setupParallax() {
        // ÏΩòÌÖêÏ∏† ÏòÅÏó≠Ïù¥ ÏóÜÏúºÎ©¥ ÏãúÏ∞® Ìö®Í≥º ÏÑ§Ï†ï Í±¥ÎÑàÎõ∞Í∏∞
        if (!this.content) return;

        const parallaxBg = this.content.querySelector('.parallax-background');
        if (!parallaxBg) return;

        const handleScroll = () => {
            const scrolled = window.scrollY;
            parallaxBg.style.transform = `translateY(${scrolled * 0.5}px)`;
        };

        window.addEventListener('scroll', handleScroll);
    }

    setupSearch() {
        const header = document.querySelector('.site-header');
        if (!header) return;

        const searchForm = header.querySelector('.search-form');
        const searchInput = header.querySelector('#search-input');
        const resultsContainer = header.querySelector('.search-results-container'); // Í∞ÄÏÉÅÏùò Í≤∞Í≥º Ïª®ÌÖåÏù¥ÎÑà

        if (!searchForm || !searchInput) return;

        let debounceTimer;

        searchInput.addEventListener('input', (e) => {
            clearTimeout(debounceTimer);
            debounceTimer = setTimeout(() => {
                const query = e.target.value;
                if (query.length > 1) {
                    this.performSearch(query, resultsContainer);
                } else if (resultsContainer) {
                    this.clearSearchResults(resultsContainer);
                }
            }, 300);
        });

        searchForm.addEventListener('submit', (e) => {
            e.preventDefault();
            const query = searchInput.value;
            if (query) {
                // Í≤ÄÏÉâ ÌéòÏù¥ÏßÄÎ°ú Î¶¨ÎîîÎ†âÏÖò ÎòêÎäî Ï¶âÏãú Í≤ÄÏÉâ Ïã§Ìñâ
                window.location.href = `/search?q=${encodeURIComponent(query)}`;
            }
        });
    }

    async performSearch(query, resultsContainer) {
        if (!resultsContainer) return;

        resultsContainer.innerHTML = '<div class="loading-spinner"></div>'; // Î°úÎî© ÌëúÏãú

        try {
            const response = await fetch(`/api/search?q=${encodeURIComponent(query)}`);
            const results = await response.json();
            this.displaySearchResults(results, resultsContainer);
        } catch (error) {
            console.error('Search failed:', error);
            resultsContainer.innerHTML = '<p class="error-message">Í≤ÄÏÉâ Ï§ë Ïò§Î•òÍ∞Ä Î∞úÏÉùÌñàÏäµÎãàÎã§.</p>';
        }
    }

    displaySearchResults(results, container) {
        if (!container) return;

        this.clearSearchResults(container);

        if (results.length === 0) {
            container.innerHTML = '<p>Í≤ÄÏÉâ Í≤∞Í≥ºÍ∞Ä ÏóÜÏäµÎãàÎã§.</p>';
            return;
        }

        const list = document.createElement('ul');
        list.className = 'search-results-list';

        results.forEach(result => {
            const item = document.createElement('li');
            item.className = 'search-result-item';
            item.innerHTML = `
                <a href="${result.url}">
                    <span class="result-title">${result.title}</span>
                    <span class="result-category">${result.category}</span>
                </a>
            `;
            list.appendChild(item);
        });

        container.appendChild(list);
    }

    clearSearchResults(container) {
        if (container) {
            container.innerHTML = '';
        }
    }

    setupFilters() {
        if (!this.content) return;
        
        const filterContainer = this.content.querySelector('.filter-container');
        if (!filterContainer) return;

        filterContainer.addEventListener('click', (e) => {
            if (e.target.matches('.filter-btn')) {
                const filter = e.target.dataset.filter;
                this.applyFilter(filter);
            } else if (e.target.matches('.sort-option')) {
                const sortBy = e.target.dataset.sort;
                this.applySorting(sortBy);
            }
        });
    }

    applyFilter(filter) {
        console.log(`Filtering by: ${filter}`);
        // ÌïÑÌÑ∞ÎßÅ Î°úÏßÅ Íµ¨ÌòÑ (Ïòà: API ÏöîÏ≤≠ ÎòêÎäî DOM Ï°∞Ïûë)
    }

    applySorting(sortBy) {
        console.log(`Sorting by: ${sortBy}`);
        // Ï†ïÎ†¨ Î°úÏßÅ Íµ¨ÌòÑ (Ïòà: API ÏöîÏ≤≠ ÎòêÎäî DOM Ï°∞Ïûë)
    }

    setupModals() {
        if (!this.content) return;

        this.content.addEventListener('click', (e) => {
            const image = e.target.closest('.modal-trigger');
            if (image) {
                e.preventDefault();
                this.openImageModal(image);
            }
        });
    }

    openImageModal(image) {
        const src = image.src || image.href;
        const alt = image.alt || 'Ïù¥ÎØ∏ÏßÄ';
        const modal = document.createElement('div');
        modal.className = 'image-modal';
        modal.innerHTML = `
            <div class="modal-backdrop"></div>
            <div class="modal-content">
                <img src="${src}" alt="${alt}" class="modal-image">
                <button class="modal-close">√ó</button>
            </div>
        `;

        document.body.appendChild(modal);

        // Î™®Îã¨ Îã´Í∏∞ Ïù¥Î≤§Ìä∏
        modal.addEventListener('click', (e) => {
            if (e.target === modal || e.target.classList.contains('modal-close')) {
                modal.remove();
            }
        });

        // ESC ÌÇ§Î°ú Îã´Í∏∞
        const handleKeydown = (e) => {
            if (e.key === 'Escape') {
                modal.remove();
                document.removeEventListener('keydown', handleKeydown);
            }
        };
        document.addEventListener('keydown', handleKeydown);
    }

    showLoadingIndicator() {
        const indicator = document.querySelector('.loading-indicator');
        if (indicator) {
            indicator.style.display = 'block';
        }
    }

    hideLoadingIndicator() {
        const indicator = document.querySelector('.loading-indicator');
        if (indicator) {
            indicator.style.display = 'none';
        }
    }

    hideLoadMoreTrigger() {
        const trigger = document.querySelector('.load-more-trigger');
        if (trigger) {
            trigger.style.display = 'none';
        }
    }

    viewItem(itemId) {
        window.location.href = `/item/${itemId}`;
    }

    // ÌéòÏù¥ÏßÄ Ï†ÑÌôò Ïï†ÎãàÎ©îÏù¥ÏÖò
    transitionToPage(url) {
        document.body.classList.add('page-transitioning');

        setTimeout(() => {
            window.location.href = url;
        }, 300);
    }
}

// Î©îÏù∏ ÏΩòÌÖêÏ∏† Ï¥àÍ∏∞Ìôî
